					<h1>Tell us about how you chose your major. </h1>
					<h2>Ever since I could remember I have always been interested in how the world works and why things happen. My own curiosity, along with my love for learning, truly inspired me to become a physicist. I fully realized that this was the major for me when I finally understood that being a physics major not only helped satisfy my cravings for knowledge, but that it also opens up endless doors of opportunity.</h2>

					<h1>Share a great experience related to your major or future career that stands out.</h1>
					<h2>One of the great opportunities for physics majors is being able to be a Teaching Assistant (TA) in some of the lower level physics labs. My work as a TA for three years definitely stands out to me as one of my greatest experiences. To me, being able to help teach and explain physics to so many students is not only rewarding, but also affirming because it reminds me how much I really enjoy what I am doing.</h2>

					<h1>If you have had an internship, please tell us that story.</h1>
					<h2>I had the fortunate opportunity to have a research position at Howard University in Washington, D.C., during the summer of 2016. This research was a Research Experience for Undergraduates (REU) program funded by the National Science Foundation (NSF). The program was 10 weeks long in the nation’s capital! At this REU program, I was studying nanophysics, specifically researching carbon nanotubes and their potential applications to lithium-ion batteries. This was easily one of the most rewarding opportunities I have had during my undergraduate career. After the summer, I was then invited to give a presentation here at JCU on my summer’s work. My education here at JCU allowed me to succeed at this research position.</h2>

					<h1>Who at JCU have been mentors for you? </h1>
					<h2>My academic Advisor, Dr. Dyck, has been a great role model for me. He not only is an excellent teacher, but he is also very willing to help students with course work, scheduling classes, or just talking about the future. He illustrates a great understanding of physics and has been very helpful in preparing me for graduate school.</h2>

					<h1>What is your favorite class at JCU so far?</h1>
					<h2>My favorite class at JCU has been MT 200: Explorations in Mathematics. It is a unique course where I studied a whole range of topics: I studied really cool number patterns, learned about the shape of the universe, talked about number theory, and even created “unbreakable” computer codes. I understand that if you aren’t into math or physics this might not be that interesting, but to me these are some of the coolest things one can learn!</h2>

					<h1>What activities and organizations are you involved with on campus?</h1>
					<h2>One of my favorite things about JCU is that there are always opportunities to get involved on campus. I was in the JCU Pep Band for two years and played at every football and basketball game! I was also an active member of the Student Union Programming Board my first two years, helping organize and execute campus-wide programs. These organizations helped me start my JCU career with a solid group of friends for whom I am very grateful. I am the vice president of the all-male a cappella group, Rhapsody Blue, which is a great way to destress and have a great time. I have  been a Resident Assistant (RA) for three years. Being an RA has helped me mold my leadership style and trained me in conflict mediation. Honestly, the JCU Office of Residence Life has offered me endless support during my undergraduate career. I am an active member of the Society of Physics Students, which helps connect all of the physics majors from freshmen to seniors. And finally, I am a member of a Greek-letter social fraternity. Being a fraternity man is definitely one of the best decisions I could have made at JCU. It gave me an excellent support group of brothers, allowed me to gain leadership experience, and has given me memories that I will never forget.</h2>
                
					<h1>What advice do you have for a high school student considering your major/career?</h1>
					<h2>The best advice I could give about being a physics major is that even though it is truly rewarding, it does take dedication. Do not be afraid to ask questions when topics get too confusing. Get to know your classmates because homework and studying is always easier in a group setting. Lastly, and in all seriousness, have fun! Physics is truly one of the coolest subjects anyone can study; it opens up so many career opportunities, it describes why the world works, and it is intellectually challenging and satisfying.</h2>
                
					<h1>Why should a high school student choose John Carroll?</h1>
					<h2>A high school student should choose JCU for a multitude of reasons! First, when picking a college you aren’t just deciding on an academic program; you are deciding on a new community. JCU’s campus is very welcoming and friendly; there always people here to say hello! JCU is a small school where all students have the opportunity to get one-on-one attention with a professor, which in most cases is invaluable. Also, the liberal arts core allows for students to be well-rounded people by taking unique classes taught from different perspectives. My favorite core classes were my theology and philosophy classes because it made me question what I thought I knew and really helped me grow as a person and an academic. Because of John Carroll’s smaller size, it gives residents a better chance to be more involved in extracurricular activities and clubs, which are perfect ways to not only enjoy your college experience but it also helps hone leadership skills. Also, JCU is great because it is only a short drive from Downtown Cleveland. There is so much for a JCU student to explore downtown, and being so close really makes this school even better!</h2>

