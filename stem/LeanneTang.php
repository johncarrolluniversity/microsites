					<h1>Share a great experience related to your major or future career that stands out.</h1>
					<h2>One of the most life changing experiences to me was my immersion trip to Honduras. I went with 19 other undergraduates, numerous staff members, and even Carroll alumni who wanted to join all of us in giving back to this wonderful community. I cannot begin to explain all that the experience has done to change me, but it definitely solidified my love of medicine and helped me realize that I was surrounded by a community both on campus and off, that would push me to be my best in all aspects of my life.</h2>
                
					<h1>If you have had an internship, please tell us that story.</h1>
					<h2>By attending JCU, I have had the opportunity to continue my research internship at the Cleveland Clinic, which I started in high school. I conduct research on Pediatric Obesity alongside my mentor and the staff of Cleveland Clinic Children’s and the BeWell Kids Clinic. I have continued to work on publications and have worked with plenty of JCU alums who have been supportive and helpful along my journey.</h2>
                
					<h1>What is your favorite class at JCU so far?</h1>
					<h2>My favorite class at JCU thus far has been Biology III Lab. The instructors have been so incredibly helpful and there is never a lack of attention or help during the course. One of the coolest experiences I have had was determining the genetic information of Cyanobacteria from Hawaii that Dr. Johansen of the Biology Department has extracted. These strands of Cyanobacteria have not been studied, and being able to be a part of such groundbreaking research in a general undergraduate biology lab is unheard of, but something that students at JCU get to conduct.</h2>
                
					<h1>What activities and organizations are you involved with on campus?</h1>
