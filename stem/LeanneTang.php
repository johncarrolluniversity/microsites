					<h1>What’s “next” for you?</h1>
					<h2>After graduation, I will be attending Ohio University’s Heritage College of Osteopathic Medicine in thanks to the Early Acceptance Program through John Carroll. For now, I hope to enjoy every moment of my undergraduate studies, continuing to be involved on campus and pushing to become an even better student and Blue Streak.</h2>
                
					<h1>What advice do you have for a high school student considering your major/career?</h1>
