					<h1>Tell us about how you chose your major.</h1>
					<h2><p>I have always been a writer, and a good one at that. From writing stories and poems, to composing literary analyses in high school, I have the talent to take my thoughts and emotions and write them down in words in an effective manner. I loved reading growing up, as well. Everything from novels to plays, poems, and musical lyrics, I recognized that writing is one of the most practical and profound forms of artistic expression that is also essential to everyday communication.</p>			
							
					<p>My freshman year at John Carroll, I originally wanted to pursue a major in psychology. While I found the subject to be interesting, it was not really a field that I was passionate about. Additionally, my freshman year English Composition teacher, Darcy Egan, recognized my writing talents and recommended that I begin the process to work in the University’s Writing Center. I was excited that someone, specifically a teacher, was acknowledging my writing talent, something I did not even recognize in myself. I reflected on my love of writing and reading from my childhood and thought that I should maybe take a minor in English.</p>
					
					<p>It was the end of my freshman year, I had just finished my Writing Center practicum, and I knew what I had to do. I saw that the only way I would be able to truly enjoy my life was if I went into a main area of study where I excel and that I love. I decided to pursue a major in Professional Writing, a practical yet artistic concentration in the English department. I began my major coursework my sophomore year with one inspiring professor after another. Maria Soriano, Bo Smith, Tom Roche, Emily Butler, David LaGuardia, and Tom Pace are examples of great instructors in the department that have made me enjoy my major every day.</p></h2> 
					               
					<h1>Share a great experience related to your major or future career that stands out.</h1>
					<h2><p>What I love about Professional Writing is how balanced it is between practicality and artistic expression. The major requires you to take both professional writing courses and literature courses. All in the same week, I am challenged to create websites, brochures, and executive summaries in one course, while reading select literature from the Augustan Age in another. The awesome aspect of this is that both of these classes are in the same department.</p>
					
               		<p>I also love how closely related the Professional Writing track is to all forms of communication, including journalism and marketing. Writing, whether creative or professional, is the basis for all forms of communication. This causes the major to perfectly complement my minors, Communication and Leadership Development. Effective communication skills are becoming increasingly important on the job market. With the combination of writing, marketing, and leadership that I am gaining from my studies, I am confident that I will be a valued asset in the professional world.</p></h2>
                
					<h1>If you have had an internship, please tell us that story.</h1>
					<h2><p>I consider my time at the John Carroll Writing Center to be an “internship,” although the Professional Writing track will require me to do a separate one to graduate. The Writing Center provides me with ways to improve my writing and the writing of others. Additionally, it has caused me to grow in terms of professionalism as it is a paid position. I have set shifts where I will come in to the center and provide one-on-one writing consultations with students and even faculty members. We collaborate to make better writing, and more importantly, better writers. The center offers help with every form of writing imaginable, including resumes, research papers, lab reports, and even creative writing. Every step of the writing process, from brainstorming to final draft revisions, may be worked on in the consultation as well.</p>
					
					<p>I am thankful for this position every day as helping others improve their writing skills is very rewarding to me. Effective writing is not a talent that everyone has. Each person is gifted with their own individual strengths, whether that be math, business, science, or writing. That is the beauty of the center. You collaborate your strengths with others to create a beautiful final product. This position affirms to me the idea that I have a skill that is needed in society and that I can use my talents to serve others.</p></h2>
                
					<h1>Who at JCU have been mentors for you?</h1>
					<h2>I have had several individuals that I consider to be my mentors at this university, both teachers and students. One of the most prominent role models for me has been Bo Smith, a professor of English. I have taken Major British Writers and Augustan Literature with him. I have never met someone who has been so passionate about both subject matter and the lives of students. He takes the lessons taught in the literature we read and applies them to real life situations. It is truly a beautiful thing to do so. Bo said that literature provides comfort for people. It gives them a reason to keep moving forward. That is something I will never forget. Bo is also very open about his past life, including his successes and failures. He shared his experiences with family, friends, and even the time he was on Jeopardy! Bo is the friendliest, most caring teacher I have ever had, with a rich history in the artistic and professional fields.</h2>
                
					<h1>What’s “next” for you?</h1>
					<h2>For my remaining time at John Carroll, I plan to focus heavily on my internship that I will need to complete for graduation. I am looking forward to being immersed even more in the professional field. After JCU, I have a variety of options due to my diverse combination of major and minors. I could do everything from medical writing to directing a play. I could receive my masters in English or continue on to law school. I am leaning toward something that involves creativity, perhaps web designing or something in the public relations field. I have never been one to worry about my future, and John Carroll has assured me that I will have no problem doing what I love while having a satisfying life.</h2>
                
					<h1>What advice do you have for a high school student considering your major/career?</h1>
