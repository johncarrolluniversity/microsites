		{
		  var tiles = document.querySelectorAll(".click");
		  for ( var i  = 0, len = tiles.length; i < len; i++ ) 
		  {
			var tile = tiles[i];
			clickListener( tile );
		  }
		
			function clickListener(card) 
		  	{
				card.addEventListener( "click", function() 
				{
					var c = this.classList;
			  		c.contains("flipped") === true ? c.remove("flipped") : c.add("flipped");
				});
			}
		})();
		
		/* instantiating a clock for the beginning of the school year */
		function clock()
		{	
			// Grab the current date
			var currentDate = new Date();

			// Set some date in the future.
			var futureDate  = new Date(2017, 7, 28, 0, 0, 0);

			// Calculate the difference in seconds between the future and current date
			var diff = futureDate.getTime() / 1000 - currentDate.getTime() / 1000;

			// Instantiate a countdown FlipClock
			var clock = $('#clockWrapper').FlipClock(diff, {
				clockFace: 'DailyCounter',
				countdown: true
			});
		}
		
		/* accurately retrieves the width of the viewport (no scrollbars) */
		function viewport() 
		{
    		var e = window, a = 'inner';
    		if (!('innerWidth' in window )) 
			{
        		a = 'client';
        		e = document.documentElement || document.body;
    		}
    		return { width : e[ a+'Width' ] , height : e[ a+'Height' ] };
		}
		
		/* decides which version of the double horizontal tile to use */
		function whichImage()
		{
			var windowWidth = viewport().width;
			
			if (windowWidth <= 1015)
			{
				$('.dbl').attr('src','images/tiles/2b.png');
				$('.dbl').parents('.click').removeClass('doublehor');
			}
			else
			{
				$('.dbl').attr('src','images/tiles/2.png');
				$('.dbl').parents('.click').addClass('doublehor');
			}
		}
		
