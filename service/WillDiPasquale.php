					<h1>Describe in more detail a specific service experience at John Carroll. What did you learn? Did the experience connect in any way to your major or career plans?</h1>
					<h2><p>This past summer, I worked an internship in Louisville, Kentucky, through John Carroll&rsquo;s Shepherd Internship Program. I was placed at a homeless services agency and its Common Assessment team to do outreach on the streets and in local shelters in order to determine who is eligible for, and most critically in need of, housing resources. I worked directly with Louisville&rsquo;s homeless population on a daily basis. As a result, I was able to develop relationships with several people who I encountered regularly. Since the availability of housing resources was so limited relative to the need, I had to ask myself how I would continue to be of value to our clients even if I couldn&rsquo;t assist in providing their most pressing need of housing.</p>
					<p>One morning during street outreach, I met Joe. Joe was living in a tent tucked away in the woods behind a row of warehouses on the margins of the city. My co-worker and I went through our normal procedure of conducting a vulnerability survey to determine his eligibility and need for re-housing. I didn&rsquo;t see Joe for a few weeks after this morning encounter. When I finally did see him again at a shelter that served daily breakfast, I approached him with a smile and said, “Hi Joe! It&rsquo;s great to see you again. How have you been?” In that moment, I had no understanding of how much that small act would mean to him. His face lit up and he couldn&rsquo;t believe that I remembered his name and wanted to know how he was doing. We proceeded to speak for over 20 minutes. He shared with me all that had been really weighing down on his soul &#8212; he had just lost two great friends, one to suicide and one to a drug overdose. He was scared and close to despair. However, he expressed to me that he was grateful for our conversation. He said that he felt so much better after talking with me.</p>
					<p>It is important to strive to eliminate the conditions under which so many individuals are able to fall into homelessness. However, one must not forget to always engage in the most fundamental and accessible form of service &#8212; the service of kindness. This was one of the most valuable experiences I&rsquo;ve ever had. I learned that I can try to engage every waking moment with a spirit of service that works to touch lives in the smallest yet often the most important ways.</p></h2>

