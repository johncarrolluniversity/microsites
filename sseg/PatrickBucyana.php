					<h1>Share a great experience related to your major or future career that stands out.</h1>
					<h2>Every political science class I have had has made my John Carroll experience an even better one. Through my classes I have met people from different backgrounds. I have also met many faculty and staff members who I know I will be able to keep in touch with even after I graduate. It is every single experience put together that makes my time at John Carroll an unforgettable one. I will never forget the amount of times I ended up in a professor’s office to ask a question about the material but ended up staying for an hour talking about what I want to do with my life in the future. It is these moments that make John Carroll different from other schools. Professors actually know their students by name and are willing to help you and make sure you succeed.</h2>
               
					<h1>If you have had an internship, please tell us that story.</h1>
					<h2>During the summer of 2016, I had the opportunity to work for the United Nations in Mozambique. I specifically worked with the United Nations Development Program. The internship could not have been at a better time and place due to the political and economic crisis going on. For the internship, I did an analysis of Mozambique’s political and economic crisis. The internship was mostly research based. I would meet, on a daily basis, with the United Nations Development Program Country Director. We talked about ways I could complete my research. My internship did not only focus on doing research on the political and economic situation of the African nation-state. I also had to find solutions the United Nations Development Program could make use of in order to deal with the issue. Through the internship, I not only reinforced my writing skills but also learned so much about the country. It opened my eyes to a lot of issues that I did not know were still prevalent in developing countries. It is definitely an experience that I will never forget.</h2>
                
					<h1>Who at JCU have been mentors for you?</h1>
					<h2>As an international student, I am privileged to have a host family. My host mom also happens to be an English professor at John Carroll. They have helped me so much. I am so grateful to have a family that looks after me like I am their own. It is through the Soft Landings Program, a support program for international students, that host parents were given to me. They have provided me with so many resources that I would not have been able to have access to if it were not for them. I am truly thankful for everything they have done and would not be the person I am today if it were not for them. They have, for example, come to pick me up from the airport every time I arrived from home during the summer. Also, they always made sure to invite me for dinner at their house during weekends or holidays throughout the year. This is not something that every international student at an American college has the opportunity to experience.</h2>
               
					<h1>What activities and organizations are you involved with on campus?</h1>
					<h2>I am currently the Vice President for Judicial Affairs and Chair of the Student Union Hearing Board. I was elected to the position in fall 2015. My job consists of overseeing different hearings, engaging in discussion with different groups involved in the hearings and most importantly, making sure that every board member is impartial and that decision-making is fair. Throughout my experience as chair of the Student Union Hearing Board, I have met different people, made friends, and also learned more about myself through others. I think that this position fits well with my plans of becoming a lawyer in the future. Being a good listener, properly using facts, and staying unbiased are things that I have learned. Other than just learning, I have enjoyed working with my peers and in doing so refined my leadership skills. Being the chair of the Student Union Hearing Board allowed me to serve my university by making sure that students in the John Carroll community better understand the Student Code of Conduct.</h2>
                
