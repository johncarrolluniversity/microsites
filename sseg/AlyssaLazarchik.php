					<h1>Share a great experience related to your major or future career that stands out.</h1>
					<h2>During my junior year, I was completing observation hours in a kindergarten classroom and helping out where I could. This involved assisting students during their literacy and math blocks throughout the day. On my first day at this new site, I was prepared, hopeful, and excited. By the end of the day I was energized by the students in the classroom and felt inspired. As if that wasn’t great enough, I overheard my cooperating teacher discussing with the principal how wonderful the Early Childhood Education program at John Carroll must be because she was extremely impressed with me. I hold on to that moment and am forever grateful for John Carroll preparing me to earn that compliment.</h2>
                
					<h1>Who at JCU have been mentors for you?</h1>
					<h2>Dr. Annie Moses is my go-to mentor at JCU. She opens her door and clears her crazy schedule if there is a student who needs her. She helped me through the times where work piled up and stress was high. I distinctly remember going to her one day when I was feeling the pressure of schoolwork more than usual. As I was voicing my frustrations, I noticed she was sorting through sheets of paper. When I had finished talking, she silently placed a binder and a stack of papers in front of me; it was all the work I had done in her previous classes. She reviewed the work with me reminding me of all the progress I have made. After I walked out of her office that day, I felt revived and proud of all that I had accomplished. Without her guidance and support I would not be where I am today.</h2>
                
					<h1>What’s “next” for you?</h1>
					<h2>For the rest of my time at JCU I will be student teaching in a kindergarten classroom in the Beachwood City Schools. I am thrilled to put all my energy into this experience and really dive in to the world of education. After graduation, I was accepted into my first graduate program where I could earn my Special Education K-8 and Reading Specialist K-12 Master of Education Degree and Certification. I feel prepared for this opportunity due to the exposure I have received from all the different types of schools I have spent time in through the education program. If I do not head straight to graduate school to further my education, I will head out on a job search with the help of the connections John Carroll has helped me make.</h2>
                
					<h1>What advice do you have for a high school student considering your major/career?</h1>
					<h2>My advice to a high school student considering being an Early Childhood Education major is to be fearless. Learning to teach involves taking risks and making many mistakes along the way. Being a risk taker is the best way to learn and you have to be ready to throw yourself into the experience.</h2>
                
					<h1>Specifically, why should a high school student choose John Carroll?</h1>
