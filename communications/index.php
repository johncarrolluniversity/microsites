			percentPosition: true
		});
		
		/* flipping cards */
		$('.click .action').click(function() 
		{
			var $parentContainer = $(this).parents('.click');
			if ($parentContainer.hasClass('flipped'))
				$parentContainer.removeClass('flipped').children('.back').delay(400).hide(0);
			else
				$parentContainer.children('.back').show(0, function() {
                    $parentContainer.addClass('flipped');
                });
		});
		
		/* hover on flip icon when on the flipped (back) side */
		$('.click .back .cardflip').mouseover(function() 
		{
			$(this).parents('.back').addClass('iconhover');
		})
		
		$('.click .back .cardflip').mouseout(function() 
		{
			$(this).parents('.back').removeClass('iconhover');
		})
		
		/* instantiating a clock for the beginning of the school year */
		function clock()
		{	
			// Grab the current date
			var currentDate = new Date();

			// Set some date in the future.
			//var futureDate  = new Date(currentDate.getFullYear() + 1, 7, 28);
            var futureDate  = new Date(2017, 7, 28, 0, 0, 0);

			// Calculate the difference in seconds between the future and current date
			var diff = futureDate.getTime() / 1000 - currentDate.getTime() / 1000;

			// Instantiate a countdown FlipClock
			var clock = $('#clockWrapper').FlipClock(diff, {
				clockFace: 'DailyCounter',
				countdown: true
			});
		}
		
