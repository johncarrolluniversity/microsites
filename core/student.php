                <h1>What was the name of the linked pair of courses you took connecting political science and philosophy?</h1>
                <h2>I took two courses that were linked as part of the Examining the Human Experience portion of the new University Core. One was a Philosophy class titled “The Pursuit of Happiness,” and the other was a Political Science class titled &ldquo;Plato’s Republic: Virtue and Order from the Athenians to the Muslim Brotherhood.&rdquo; </h2>
                
                <h1>What did you think of them at the start of the semester versus at the end of the semester?</h1>
                <h2>Right away on my first day of class, it was clear my linked courses would be different than any other class I’ve taken at John Carroll. The most notable difference was the two classes were co-taught, meaning the professors participated almost like students in the other class. I definitely thought this was strange at the beginning, and I remember feeling unsure how effective this dynamic could be for the semester.
                <br/> <br/>
Because they were linked, the required readings for both courses matched. So, we started with the ancient Greeks for both classes. I had to read Plato’s Republic for both professors to prepare for lectures and discussion. My Philosophy class analyzed it to determine what justice is, how humans find happiness in their lives, and how society is structured consequently. Then, immediately after that discussion ended, the class switched to Political Science, and we discussed how the style of rule Plato advocated for in his writing enabled humans to be happy. It was a subtle shift in perspective, but it definitely forced me and my classmates to be flexible. This particular unit taught me that a well-structured and ruled society is a product of humans achieving happiness. But I also learned a well-ruled society is a significant cause of humans achieving happiness. Similarly, I read Candide by Voltaire in both classes and had discussions about &ldquo;the good life&rdquo; from an individual, philosophical prospective and from a state-centric perspective. 
<br/><br/>
I think what I learned with both professors is best summarized with two words: cognitive dissonance. These classes encouraged me to hold two different, contrasting opinions at once, and they taught me how to investigate without denouncing either opinion. 
<br/><br/>
At the end of the semester, the two linked classes were my favorite; I looked forward to my Tuesday/Thursday schedule all week because I loved the discussions. I think it was a great way to encourage students to explore their talents and interests in the classroom.
</h2>
                
                <h1>How do you feel the new Core prepares you for the future?</h1>
                <h2>It is encouraging more mindfulness in education. Intellect grows the most when stretched, and the inclusion of the linked courses definitely help students make greater strides with their critical thinking skills than they would by participating in one of the two combined classes unaccompanied by the other. </h2>
           
