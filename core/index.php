		function calcHeight()
		{
			var height = 0;
			var totalHeight = 0;
			var tempMargin = 0;
			var tileCount = 0;

			$("li.click").each(function()
			{
				// get width, assign to height & add all up
				height = $(this).width();
				totalHeight += height;
				tileCount++;

				// get margins
				tempMargin = parseInt( $(this).css("marginBottom"));
				totalHeight += tempMargin;

				// assign height for each li
				$(this).css("height",height);
			});

			$("li.double").css("height",(height*2)+tempMargin);

			// calculating how many tiles per row
			var clickWidth = parseInt($("li.click").css("width"));
			var gridWidth = $("#grid").width();

			var percentage = ((clickWidth/gridWidth)*100).toFixed(1);
			var t = 0;
			var extra = 0;
			//tileCount--;


			if (percentage <= 33)
			{
				extra = tileCount % 3;
				if (extra != 0)
				{
					totalHeight -= (extra * height) + (extra * tempMargin);
					t = (totalHeight / 3) + height + tempMargin;
				}
				else
					t = totalHeight / 3;
			}
			else if (percentage > 33 && percentage <= 50)
			{
				extra = tileCount % 2;
				if (extra != 0)
				{
					totalHeight -= (extra * height) + (extra * tempMargin);
					t = (totalHeight / 2) + height + tempMargin;
				}
				else
					t = totalHeight / 2;
			}
			else
			{
				t = totalHeight;
			}

			// assign heights
			$("#grid").css("height", t);
			$("#content").css("marginTop", 100);
			$("#content").css("marginBottom", 100);
			//$("#contentwrapper").css("width", clickWidth*3);
		}

		$(window).ready(function()
		{
			calcHeight();
		});

		$(window).resize(function()
		{
  			calcHeight();
		});

		(function()
		{
		  var tiles = document.querySelectorAll(".click");
		  for ( var i  = 0, len = tiles.length; i < len; i++ )
		  {
			var tile = tiles[i];
			clickListener( tile );
		  }

			function clickListener(card)
		  	{
				card.addEventListener( "click", function()
				{
					var c = this.classList;
			  		c.contains("flipped") === true ? c.remove("flipped") : c.add("flipped");
				});
			}
		})();
	</script>

