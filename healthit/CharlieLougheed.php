                While he was in high school, Charlie Lougheed &rsquo;91 wasn’t even sure he wanted to go to college. He planned to continue developing a computer business he created. After carefully considering advice from his parents and high school counselor, he decided going to college was his best choice. That advice has proven true.
                <br/><br/>
Since graduating from JCU, Lougheed has helped revolutionize medical databases with a cloud-based analytical technology company called Explorys, which he helped found.
				<br/><br/>
&ldquo;People thought we were crazy,&rdquo; Lougheed says. &ldquo;They told us we didn&rsquo;t know anything about health care, and that, because of security concerns and traditional closed system thinking, there was no way hospitals would put data in the cloud. But we saw an opportunity to change that. We ended up way ahead of the market, which later made it difficult for competitors to catch up.&rdquo;
				<br/><br/>
Before Explorys, the process of sharing data between hospitals and health-care systems was tedious, time consuming, and fraught with difficulties. Now, any participating health-care system with appropriate privacy rights can access information, see trends, and identify correlations on their patient population and outcomes in seconds. Within six years of its founding, Explorys expanded to 360 hospitals nationwide, involving more than 50 million patients and 300,000 doctors.
				<br/><br/>
Explorys enables those in health care to improve the quality and efficiency of care by turning big data into information that can be appropriately shared and acted on. The company assembles key data for patients that provide insights into what actions can be taken to improve their clinical outcomes based on sophisticated prediction algorithms. Explorys&rsquo; platform provides a secure data cloud-computing superstructure that can outperform traditional data warehouse capacity, speed, throughput, and cost efficiency, enabling health-care and life science leaders to improve their businesses.
				<br/><br/>
In 2015, IBM acquired Explorys as a cornerstone to Watson Health, IBM’s cognitive computing unit. Explorys has grown into one of the largest medical databases in the world, making actionable information more readily available to those who can make a difference in the lives of millions of Americans.
