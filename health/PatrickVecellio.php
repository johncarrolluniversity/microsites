                <h1>Is there a course you took at JCU that was unique or that you particularly enjoyed?</h1>
                <h2>Without a doubt, my favorite course was a special topics course offered by the history department. It was a one-credit weekend class in Gettysburg, Pennsylvania taught by the one and only Dr. George Vourlojianis. He provided a thorough and unbelievably entertaining historical experience. For the more science-oriented people, genetics was my favorite (and most difficult) course in the sciences. With such a rapidly advancing field, Dr. Sean Kessler was able to contextualize the concepts in real world applications.</h2>
                
                <h1>What aspect of our pre-health professions program and/or science departments helped prepare you for medical school?</h1>
                <h2>I truly did not appreciate the value of having such engaged faculty at JCU. One thing that has separated me from many of my [current] classmates is my experience and ability to interact with faculty, many of whom are physicians in med school (NETWORKING!!). I couldn’t imagine my John Carroll experience without the mentorships and friendships that I have built with many professors there. <br/><br/>
The science doesn’t change college to college. Genetics at JCU is the same genetics that is taught at other schools. The difference comes in the ease of asking questions, actively grappling with difficult applications, and being able to critically think through them. The ability to access faculty, develop relationships, and to tangibly see their investment in my learning was the best motivation for the difficult and long road into the medical field.</h2>
                
                <h1>Has there been a truly meaningful experience you’ve had at JCU that is related to pre-health?</h1>
                <h2>My immersion experience in Honduras during summer 2015 was one of the most meaningful pre-health activities that I was involved with at JCU. Of course, research is important (and the Cleveland Clinic sounds very good in that aspect!), but my more service-oriented experiences, like the Honduras immersion, were much more than a resume builder. They provided me with a learning [experience] that can never be taught in a classroom. The opportunity to engage with diverse populations and develop a compassionate approach in an attempt to truly help others was more beneficial than any studying could be.</h2>
                
                <h1>How did JCU help you get into med school?</h1>
                <h2>The Health Professions Advisory Committee (HPAC) process was an unbelievable resource. Not only was the committee formed for me (my friends who went to big state schools had a lot of difficulty tracking down faculty for their committees), but the mock interviews were also perfect preparation for the actual medical school interviews. I felt confident and prepared for anything that could be asked. <br/><br/>
The actual interviews ended up being far easier than the HPAC ones; that is a testament to the value of our pre-health program. Dr. George Lewandowki’s involvement (our physician-in-residence) was also invaluable. While he helps students formulate their personal statements, essays, and secondaries, his advice and just positive demeanor were so comforting during the difficult process that is medical school applications. He reminds me why I want to become a physician, to be of service to others – just like him. </h2>
                
                <h1>Anything else you’d like to add that you feel is pertinent?</h1>
                <h2>I can’t stress the importance of “having people in your corner.” It is a long road and it will be difficult at times. The support of family, friends, and a school like JCU made it a million times easier. John Carroll will forever be my home. Onward On. </h2>

