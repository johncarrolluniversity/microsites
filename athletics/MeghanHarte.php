					<h1>What advice do you have for a high school student who wants to play a sport during college?</h1><br />
					<h2>I would tell all high school students to keep their options open when looking into playing a sport in college. It can be very time consuming juggling different coaches and visiting all of the schools, but be patient! I did not have JCU on my top list of schools until January of my senior year. The right school will come your way. Finally, pick a school where you could see yourself going even if you did not play a sport there. We need to love the school for the school and not just for the athletics!</h2>

