					<h1>Has your athletic participation shaped your educational experience?</h1><br />
					<h2>Every John Carroll athlete is held to a high standard on and off of the field of having good character. When we travel, other schools are judging the whole University off of the way the team presents itself. Through this, each team has been known for showing great character. Our team prides itself on being respectful to all of our opponents. This shines through after the last whistle of every game. Our team carries itself with pride through shaking hands with the other team. Even if the result did not go our way and the game was lost from uncontrollable factors, we know we left everything on the field. For me, this speaks volumes for the character our team and athletic department has.</h2>
					
					<h1>Is it hard to balance school and sports in college?</h1><br />
					<h2>Being a college athlete will always be challenging, but JCU tries to make the transition from high school to JCU as smooth as possible. We, the student-athletes of John Carroll, are students first, then athletes. Our coaches realize we are here to get an education. They are supportive when we have a midterm or an important project. They help in many ways, they sometimes even allow us to leave practice a little early, or show up late. On top of that, our professors here also want us to succeed. They provide office hours to help with questions outside of class. If you cannot make them, they will schedule another time to meet with you! Finally, there are also tutors nightly in the library to help with any assignment needed. With this, we also want to be successful in sports. We have a strength and conditioning coach who helps prepare us for the season. With the support of our professors, coaches, teammates, and strength and conditioning coach, balancing school and sports here is easy to manage!</h2>
					
