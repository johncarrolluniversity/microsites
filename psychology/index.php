		  for ( var i  = 0, len = tiles.length; i < len; i++ )
		  {
			var tile = tiles[i];
			clickListener( tile );
		  }

			function clickListener(card)
		  	{
				card.addEventListener( "click", function()
				{
					var c = this.classList;
			  		c.contains("flipped") === true ? c.remove("flipped") : c.add("flipped");
				});
			}
		})();
