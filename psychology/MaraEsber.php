                <h1>Is there a particular course you took at JCU that was unique or that you particularly enjoyed?</h1>
                <h2>Child and Adolescent Psychopathology with Dr. Masterson. I took this course my sophomore year and it confirmed for me that I want to work with children and their families in a clinical setting. Also, Dr. Masterson’s passion and love for her work that she conveyed to our class instilled in me a passion of my own that I wanted to pursue. </h2>

                <h1>What helped prepare you for the next step of going on to graduate school?  </h1>
                <h2>I think the most instrumental part of John Carroll’s psychology program is the range of internship opportunities available for students. Our professors are connected in the field, and when they become aware of an opportunity, they share it readily with their students. As an undergraduate, I had hands-on clinical experience at the Cleveland Clinic and so did many of my peers. Our unique experiences aided in classroom discussions when we would discuss topics of therapy or interventions that we used in our internships.</h2>

                <h1>What about the Boler School, in general, helped prepare you for what's next?</h1>
                <h2>The Boler Professional Development (BPD) Program – From freshman to senior year, BPD classes taught me how to develop a professional presence in the business world. </h2>

                <h1>Has there been a truly meaningful experience you’ve had in your years at JCU that is related to psychology?</h1>
                <h2>I went on an immersion trip to Ecuador not knowing any Spanish. At times, I was frustrated not being able to communicate what I wanted to say in Spanish. This was very humbling because it made me realize that the children with autism I was working with, who are nonverbal, experienced not being able to express what they wanted to say with words every day. It gave me a newfound sense of empathy coming home and continuing to work with these children.</h2>

                <h1>How did JCU help you get into grad school?</h1>
                <h2>Most of the graduate schools I applied to asked for three letters of recommendations, two of them being from faculty members. When it was time for me to ask my professors to write letters [for me], I felt confident that they knew me not only as a student, but as a person. I feel that this is a product of the small classroom sizes as well as the availability and commitment of our professors.</h2>

                <h1>Anything else you’d like to add that you feel is pertinent?</h1>
                <h2>I found out a John Carroll alum attended my first choice graduate school and reached out to him. He emailed me before my interview to give me some pointers, and then gave me advice on how to succeed once I was accepted! I hope I can return the favor to a future Blue Streak one day when I am out in the field, too!</h2>
