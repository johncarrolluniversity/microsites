					<h2>Whenever I have visitors in town I always make sure to take them to one of the Cleveland signs located around the city. There are currently three locations: Edgewater Park, North Coast Harbor, and Tremont. They are all prime picture-taking spots and also a good way to show visitors different parts of the city.</h2>
					
					<h1>What&rsquo;s the best restaurant to visit? What&rsquo;s your go-to perfect meal there?</h1>
					<h2>My two favorite restaurants in/around the city are Barrio and Mia Bella. Barrio has fantastic $3 make-your-own tacos which are just up my alley as a picky eater. Their queso and salsa are great compliments to the meal as well. Mia Bella in Little Italy is by far the best Italian food I&rsquo;ve ever had. I always get their award-winning dish, the cheese tortellini.</h2>

					<h1>Share a favorite memory/event/experience from your time in Cleveland.</h1>
					<h2>In the last three years here in Cleveland, my favorite memory was being downtown during the Cubs vs. Indians World Series. I come from a baseball family so being able to be downtown right next to the stadium while the games were being played was a thrilling experience. There was a big watch party set up right outside the stadium so you were able to watch the game on the screen and also hear the action from the stadium. It was astonishing to see so many John Carroll students down there as well. I felt I could not turn a corner without knowing someone from school.</h2>
                
					<h1>What are the various ways you take advantage of Cleveland (service, fun, sports, internships)?</h1>
					<h2>Being a sports fanatic it has been exciting to only be about 20 minutes from three major sports teams. Being a baseball fan I have been to many Indians games and got to experience the excitement of the World Series being here! I have been to Cavs games in the past, as well, and I am hoping to make it to a Browns game before my time at John Carroll is up.</h2>
                
					<h1>Overall, JCU helps our students develop intellect, character, leadership, and service in their time on campus. How has Cleveland played a role in these learning goals for you?</h1>
					<h2>Being in the Cleveland area has shaped me into the person I am today. Originally from Buffalo, I was nervous that it would be difficult to get involved in activities outside of John Carroll because I would not know enough about the city. The exact opposite has proven to be true. Because I was not familiar with the city I was pushed to go outside of my comfort zone to get involved and Carroll was just the right place to start. Through JCU, I have been exposed to a variety of opportunities. I have been able to take leadership roles while doing service in the communities around Carroll. Also, with Cleveland recently hosting the RNC, it broadly widened my knowledge of local politics that are happening around me. Overall, being in a new city has allowed me develop these aspects of myself that I am constantly looking to improve upon. </h2>
                
					<h1>Why should a student from farther away come here?</h1>
					<h2>I am from the Buffalo area and wanted to go far enough away for college where I could not go home whenever I was feeling a little homesick, but also close enough to be able to go home for a weekend when needed. This has pushed me to make friends here on campus so that the constant want to go home went away quickly after the first few weeks freshman year. There are so many students from the Buffalo area, though, so when I do want to go home I can always find someone to travel with. John Carroll has really become a home away from home. I have made a friend group that is like family to me. There are also many staff members that are there for the guidance that I miss receiving from my parents. Between the red brick buildings, the friendly staff, and always being able to walk around campus and see a familiar face, John Carroll really draws you in and makes you a part of a greater community outside of where you came from. </h2>

