					<h1>What&rsquo;s the best restaurant to visit? What&rsquo;s your go-to perfect meal there?</h1>
					<h2>Tommy&rsquo;s. It&rsquo;s this restaurant in Coventry with a casual diner feel known for its milkshakes and in-restaurant access to the bookstore next door. It reminds me of the home-style cooking that I so often miss! My go-to meal is definitely the BLT with a mixed peanut butter and raspberry milkshake (trust me, it&rsquo;s better than you&rsquo;d think!). And if I&rsquo;m feeling especially hungry, I&rsquo;ll also get a cup of their homemade French onion soup.</h2>

					<h1>Share a favorite memory/event/experience from your time in Cleveland.</h1>
					<h2>My favorite memory would be when, over the summer, my fellow interns and I met at this little lakeside restaurant next to some volleyball courts. We had this great idea to stuff ourselves with casual grill meals and then head to the courts to show our stuff. Needless to say, we were all competitive and it made it all the more interesting! There was plenty of laughter involved and a lot of sore stomachs from all the action!</h2>
                
					<h1>What are the various ways you take advantage of Cleveland (service, fun, sports, internships)?</h1>
					<h2>Being a psychology major in my senior year, I&rsquo;m diving heavily into internships. I had one this past summer with the Cleveland Clinic Children&rsquo;s Center for Autism and I have one this spring through a local school known as Julie Billart which specializes in educating student with a variety of special needs. I did a lot of networking my junior year and it definitely paid off. The internship I did over the summer through the Clinic helped to introduce me to a variety of people that were able to point me in the direction of my spring internship. Networking in Cleveland seems to be an easy task because it appears to be a part of the city&rsquo;s culture and that is definitely something to take advantage of. In regards to fun, Coventry has this place where local bands perform and are often times better than you expect. And Playhouse Square hosts some of the best shows. My favorite was going to see Disney&rsquo;s “Beauty and the Beast” with my resident assistant staff. We not only enjoyed the play and the sly adult humor, but we also had a wonderful bonding experience!</h2>
                
					<h1>Overall, JCU helps our students develop intellect, character, leadership, and service in their time on campus. How has Cleveland played a role in these learning goals for you?</h1>
					<h2>Cleveland has opened its arms to me and enabled me to step outside of my comfort zone. I&rsquo;ve always been one to stay in and study, but Cleveland and its people have encouraged me to go out and learn from those around me. Learn from the neighborhoods and the diverse cultural areas that exist not even 15 minutes away! The city is so heartwarming and I&rsquo;ve even had classes that encourage us to go downtown in order to complete an assignment, whether that&rsquo;s at the Cleveland Film Festival every spring or a court case at the justice center. It&rsquo;s never a dull moment in Cleveland and there are always numerous opportunities to help those in the community around you.</h2>
                
					<h1>Why should a local student stay here for college?</h1>
