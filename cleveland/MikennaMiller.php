					<h1>Why should a student from farther away come here?</h1>
					<h2>Cleveland is such an up-and-coming city. It has so much potential and so much to offer that it makes it a wonderful place to be as a college student. And even if you&rsquo;re not a big city fan, the suburbs have plenty to do! I personally miss my parks and hometown restaurants and so I find myself walking the Metroparks or driving down Mayfield Road looking for a new place to eat!</h2>

