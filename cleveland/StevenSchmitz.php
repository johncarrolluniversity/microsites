					<p>I have been able to do service throughout the city of Cleveland; see plays at Playhouse Square; attend Browns, Cavs, or Indians games; and visit the Rock and Roll Hall of Fame, Cleveland Zoo, and the science center all in two years at JCU. There is no other way I would want to spend my weekends at John Carroll then being in the city.</p> 
