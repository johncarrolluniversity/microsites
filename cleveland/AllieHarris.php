					<h1>Tell us about your favorite spot(s) in the city.</h1>
					<h2>My favorite spot is the West Side Market in Ohio City. It&rsquo;s a huge indoor market with lots to walk around and see. I love to go there on a Saturday or Sunday morning. They sell a lot of local specialty food items so it is also great to stock your fridge with your favorite snacks from there. A lot of my favorite things involve eating, but Cleveland has so many good food options that it&rsquo;s hard not to find good food wherever you go. </h2>
					
					<h1>What&rsquo;s the best restaurant to visit? What&rsquo;s your go-to perfect meal there?</h1>
					<h2>Barrio is my all-time favorite restaurant. Since you build your own tacos, I try to switch it up and try something new every time I go. The tacos are only $3, so it is really nice on a college student budget. Plus, it&rsquo;s a great place to go with friends, even just to get the chips and queso and hang out.</h2>

					<h1>Share a favorite memory/event/experience from your time in Cleveland.</h1>
					<h2>It&rsquo;s a really exciting time to be living in Cleveland, especially seeing the recent success of our local sports teams. I was living at John Carroll over the summer working as an orientation leader, so I was on campus for the 2016 NBA Finals. Everyone on orientation staff would get together in the lounge of Murphy Hall to watch the games together. Some of us were from the Cleveland area, while a lot of us weren&rsquo;t, but we were all so excited to see the Cavs win the championship. </h2>
                
					<h1>What are the various ways you take advantage of Cleveland (service, fun, sports, internships)?</h1>
					<h2>Being a business student, there are so many options for gaining professional experience so close to John Carroll. I&rsquo;ve been able to have internship experiences that have given me hands-on practice in my field of study. This is so important, since as students, we are still making decisions about what we want to do after graduation. The best way to find out if you are interested in something is to try it. My internship experience actually directed me to my interest in the non-profit field.</h2>
                
					<h1>Overall, JCU helps our students develop intellect, character, leadership, and service in their time on campus. How has Cleveland played a role in these learning goals for you?</h1>
					<h2>Cleveland has opened my eyes in so many ways. My hometown is a very small, rural community. While the “city” environment was intimidating at first, I quickly saw how much opportunity was within it. I am never bored on the weekends because there is always something to do. While I used to not see myself as a “city” person, Cleveland was such an exciting change of pace. I even spent four months studying in London, so I&rsquo;ve really grown to love exploring different cities while appreciating my small town background.</h2>
                
					<h1>Why should a local student stay here for college?</h1>
					<h2>There is a whole community available to local students that they can immerse themselves in through becoming a part of the John Carroll community. John Carroll offers so many options for students to become involved in service and leadership. While students may be local, John Carroll will open students to a whole new perspective within their own community.</h2>
                
					<h1>Why should a student from farther away come here?</h1>
					<h2>Being a non-local student, I have found a home at John Carroll and in Cleveland. I quickly became connected to the community during my freshman year, and have spent all my time since finding more ways to continue expanding on these connections. I have gotten to try so many new experiences with my JCU friends because going to college in Cleveland allowed me to explore a whole new city as well!</h2>

